            function cuiIsValid(cui) {
                var cuiRegExp = /^[0-9]{4}\s?[0-9]{5}\s?[0-9]{4}$/;
                if (!cuiRegExp.test(cui)) {
                    return false;
                }

                cui = cui.replace(/\s/, '');
                var depto = parseInt(cui.substring(9, 11), 10);
                var muni = parseInt(cui.substring(11, 13));
                var numero = cui.substring(0, 8);
                var verificador = parseInt(cui.substring(8, 9));

                // Listado de municipios por departamento
                var munisPorDepto = [
                    17, 8, 16, 16, 13, 14, 19, 8, 24, 21, 9, 30, 32, 21, 8, 17, 14, 5, 11, 11, 7, 17
                ];

                if (depto === 0 || muni === 0 || depto > munisPorDepto.length || muni > munisPorDepto[depto - 1]) {
                    return false;
                }

                // Verificación del correlativo con el algoritmo del complemento 11
                var total = 0;
                for (var i = 0; i < numero.length; i++) {
                    total += numero[i] * (i + 2);
                }
                var modulo = (total % 11);

                return modulo === verificador;
            }
        });

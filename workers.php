                var $parent = $(this).parent();

                if (cuiIsValid(cui)) {
                    $parent.addClass('has-success');
                    $parent.removeClass('has-error');
                    $parent.find('.form-control-feedback').addClass('glyphicon-ok');
                    $parent.find('.form-control-feedback').removeClass('glyphicon-remove');
                } else {
                    $parent.addClass('has-error');
